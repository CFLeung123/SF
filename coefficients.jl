function R0(f)
    R0f = Array{ Double64}(undef, length(f)-delta)
    for L in 1:length(f)-delta
        R0f[L]=L/delta*(f[L+delta]-f[L])
    end
    return R0f
end

function Rnu(nu,f)
    Rnuf = Array{ Double64}(undef, length(f)-delta)
    for L in 1:length(f)-delta
        Rnuf[L] = f[L] + L/(nu*delta)*(f[L+delta]-f[L])
    end
    return Rnuf
end

